void TIDrawIcon(Adafruit_GFX& d, uint16_t id, uint8_t x, uint8_t y, uint8_t frame, bool daytime, bool transparent, uint16_t backgroundColor) {
  uint16_t** animation;
  if (id >= 200 && id < 300) {
    animation = thun_ani;
  } else if (id >= 300 && id < 600) {
    animation = rain_ani;
  } else if (id >= 600 && id < 700) {
    animation = snow_ani;
  } else if (id >= 700 && id < 800) {
    animation = ovct_ani;
  } else if (id == 800) {
    if (daytime)
      animation = suny_ani;
    else
      animation = mony_ani;
  } else if (id >= 800 && id < 900) {
    animation = clod_ani;
  } else {
    Serial.print("TinyIcons: Unknown weather code: ");
    Serial.println(id);
    return;
  }
  
  if (frame > 4) {
    Serial.print("TinyIcons: Animation frame too high: ");
    Serial.println(frame);
    return;
  }

  uint i, j;
  for (i = 0; i < 5; i++) 
    for (j = 0; j < 10; j++)  {
      uint16_t color = animation[frame][i * 10 + j];
      if (color != 0) {
        d.drawPixel(x + j, y + i, color);
      } else if(!transparent) {
        d.drawPixel(x + j, y + i, backgroundColor);
      }        
    }
}